		<h2 class="text-2xl md:text-3xl font-bold text-center mb-8">The Legal Standard</h2>
		<div class="space-y-5 text-gray-200 leading-relaxed text-lg">
			<p>
				There is a common misconception about snow and ice liability. Many business owners believe that if someone slips on their property, they are automatically liable. That is not how the law works.
			</p>
			<p>
				In negligence cases involving snow and ice, the legal standard is <strong class="text-white">not</strong> whether the premises were completely clear of all snow and ice at all times. The standard is whether the business had a <strong class="text-white">reasonable process</strong> in place to address snow and ice conditions, and whether that process was <strong class="text-white">actually followed</strong>.
			</p>
			<p>
				This is exactly what SafeWalk helps you establish. A written policy shows you have a process. An activity log proves you followed it. An employee poster demonstrates your staff was trained. Together, these documents create the evidentiary foundation that defense attorneys rely on when protecting businesses from slip-and-fall claims.
			</p>
